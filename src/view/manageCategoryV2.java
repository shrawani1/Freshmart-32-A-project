    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addBtnActionPerformed
        String id = catsntf.getText();
        String name = catnametf.getText();
        String desc = catdescriptiontf.getText();

        CategoryV2 c1 = new CategoryV2(Integer.parseInt(id), name, desc);
        categoryController cc = new categoryController();
        int result = cc.insertdetails(c1);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Added Successfully");
            display();
        }
    }// GEN-LAST:event_addBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_editBtnActionPerformed
        String id = catsntf.getText();
        String name = catnametf.getText();
        String desc = catdescriptiontf.getText();

        CategoryV2 c1 = new CategoryV2(Integer.parseInt(id), name, desc);
        categoryController cc = new categoryController();
        int result = cc.updateDetails(c1);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Updated");
            display();
        }
    }// GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_deleteBtnActionPerformed
        String id = catsntf.getText();
        ;

        CategoryV2 c1 = new CategoryV2(Integer.parseInt(id), null, null);
        categoryController cc = new categoryController();
        int result = cc.deleteDetails(c1);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Deleted Successfuly");
            display();
        }

    }// GEN-LAST:event_deleteBtnActionPerformed

    public void display() {
        ResultSet result = new categoryController().fetchDetails();
        DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
        model.setRowCount(0);
        try {
            while (result.next()) {
                String id = result.getString(1);
                String name = result.getString(2);
                String desc = result.getString(3);

                Object[] rows = { id, name, desc };
                model.addRow(rows);
            }
        } catch (SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }


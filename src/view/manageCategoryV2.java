    private void categoryTableMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_categoryTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) categoryTable.getModel();
        String tbleSn = tblModel.getValueAt(categoryTable.getSelectedRow(), 0).toString();
        String tbleName = tblModel.getValueAt(categoryTable.getSelectedRow(), 1).toString();
        String tbleDescription = tblModel.getValueAt(categoryTable.getSelectedRow(), 2).toString();

        catsntf.setText(tbleSn);
        catnametf.setText(tbleName);
        catdescriptiontf.setText(tbleDescription);
    }// GEN-LAST:event_categoryTableMouseClicked

